SELECT
202001 AS MONTH_ID,A.PRVNCE_ID,A.LATN_ID,A.KEY_TARGET_CD,A.TARGET_UNIT
,SUM(CASE WHEN A.MONTH_ID=202001 THEN A.TARGET_VALUE END) AS TARGET_VALUE
,SUM(CASE WHEN A.MONTH_ID=TO_CHAR(ADD_MONTHS(TO_DATE('202001','YYYYMM'),-1),'YYYYMM') THEN A.TARGET_VALUE END) AS TARGET_VALUE_LASTMONTH
FROM ODSPMART.T_BWT_DAPM_PRD_REPORT_CLCTTGT2 A
WHERE A.MONTH_ID>=TO_CHAR(ADD_MONTHS(TO_DATE('202001','YYYYMM'),-1),'YYYYMM')
AND A.KEY_TARGET_CD IN ('M1010101001','M1010103013','M1010103031','M1010202051','M1010202060','M1010202061','M1010202062')
GROUP BY A.PRVNCE_ID,A.LATN_ID,A.KEY_TARGET_CD,A.TARGET_UNIT
ORDER BY A.KEY_TARGET_CD;


